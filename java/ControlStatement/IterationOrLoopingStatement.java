package ControlStatement;

public class IterationOrLoopingStatement {
	
public static void main(String[] args) {
	
//	while loop
	
	int i = 10;
	
	while(i>=1)
	{
		System.out.println("Here is the sequence ->"+i);
		i--;
		
	}
	
	
	int p = 1; //start point 
	
	while(p<=10) //end point
	{
		System.out.println(p);
		p++; //iteration
	}
	
//	do-while loop
	
	int q = 1;
	
	do
	{
		System.out.println(q);
		q++;
	}
	while(q<=10);
	
	
	
	
//	for loop
	
	
	
	for(int r = 20;r<=30;r++) 
	{
		System.out.println(r);
	}
	
	
	
	
	
	
	
	
	
	
	
	
	
}
}
