package Session_4.loops;

public class _3_PatternPrint {
	
	public static void main(String[] args) {
		
//   *
//   **
//   ***
//   ****
		
		for(int i =0; i<=3;i++) 
		{
			for(int j =0;j<=i;j++) 
			{
			System.out.print("*");	
			}
			System.out.println(" ");
		}
		
		
//		
//		****
//		***
//		**
//		*
	
		for(int k =3;k>=0;k--)
		{
			for(int m = 0;m<=k;m++)
			{
				System.out.print("*");
			}
			System.out.println(" ");
		}

		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
