package session_2;

public class _2_IncAndDecOperator 
{
	public static void main(String[] args)
	{
		// ++ it increase the value by one
		//-- it decrease the value by one
		
		// pre incre
		int p = 1;
		int q = ++p;
		System.out.println(p);//2
		System.out.println(q);//2
		
		
		//post incre
		
		int a = 1;
		int b = a++;
		System.out.println(a);//2
		System.out.println(b);//1
		
		
		//pre decre
		
		int x = 2;
		int y = --x;
		System.out.println(x);//1
		System.out.println(y);//1
		
		
	//post decre
	  
		int i1 = -2;
	int i2 = i1--;
		
	System.out.println(i1);//-3
		System.out.println(i2);//-2
		
		
		char abc = 'A';
		System.out.println(++abc);//B
		System.out.println(abc++);//B
		
		
		char xyz = 'B';
		
		System.out.println(--xyz);// A
		System.out.println(xyz--);// A
		
		System.out.println(2+xyz);// 68
		
		//System.out.println(++A); // not a valid statement ( we did operation by using variable) 
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
