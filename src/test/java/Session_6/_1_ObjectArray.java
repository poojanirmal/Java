package Session_6;

public class _1_ObjectArray {
	
	public static void main(String[] args) {
		
//		Array - it can stored multiple value of same type of data
		
//		Object Array - it can stored multiple value of different type of data
		
//		serial no. to output
		
		Object emp[] = new Object[5];
		
		emp[0] = "Tom";
		emp[1] = 27;
		emp[2] = 499.99;
		emp[3] = 'M';
		emp[4] = false;
		
		for(int i = 0;i<emp.length;i++)
		{
			System.out.println((i+1)+". "+emp[i]);
		}
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
